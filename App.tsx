
import React, { useState, useEffect } from 'react';
import { HashRouter, Routes, Route, useLocation } from 'react-router-dom';
import Home from './pages/Home';
import About from './pages/About';
import Locations from './pages/Locations';
import LocationDetail from './pages/LocationDetail';
import Contact from './pages/Contact';
import Gallery from './pages/Gallery';
import Navbar from './components/Navbar';
import Footer from './components/Footer';
import Loader from './components/Loader';

const AppContent = () => {
  const [isInitialLoad, setIsInitialLoad] = useState(true);
  const [isPageLoading, setIsPageLoading] = useState(false);
  const location = useLocation();

  // Initial Load simulation
  useEffect(() => {
    const timer = setTimeout(() => {
      setIsInitialLoad(false);
    }, 2000);
    return () => clearTimeout(timer);
  }, []);

  // Route change simulation
  useEffect(() => {
    if (!isInitialLoad) {
      setIsPageLoading(true);
      window.scrollTo(0, 0);
      const timer = setTimeout(() => {
        setIsPageLoading(false);
      }, 800);
      return () => clearTimeout(timer);
    }
  }, [location.pathname]);

  if (isInitialLoad) {
    return <Loader fullScreen={true} />;
  }

  return (
    <div className="flex flex-col min-h-screen relative">
      {/* Page-to-page loader overlay */}
      {isPageLoading && (
        <div className="fixed inset-0 z-[60] bg-bg-beige/60 backdrop-blur-sm flex items-center justify-center animate-in fade-in duration-300">
          <div className="flex flex-col items-center gap-4">
            <div className="size-12 bg-primary rounded-lg flex items-center justify-center text-white animate-bounce">
              <span className="material-symbols-outlined text-2xl">eco</span>
            </div>
            <span className="text-primary font-bold text-[10px] tracking-widest uppercase">Preparing...</span>
          </div>
        </div>
      )}

      <Navbar />
      <main className={`flex-grow transition-opacity duration-500 ${isPageLoading ? 'opacity-20' : 'opacity-100'}`}>
        <Routes>
          <Route path="/" element={<Home />} />
          <Route path="/about" element={<About />} />
          <Route path="/locations" element={<Locations />} />
          <Route path="/locations/:id" element={<LocationDetail />} />
          <Route path="/contact" element={<Contact />} />
          <Route path="/gallery" element={<Gallery />} />
        </Routes>
      </main>
      <Footer />

      {/* Global WhatsApp CTA */}
      <a
        href="https://wa.me/919509205025" 
        target="_blank"
        rel="noopener noreferrer"
        className="fixed bottom-8 right-8 z-[100] group flex items-center gap-4"
        aria-label="Contact us on WhatsApp"
      >
        <span className="hidden md:block bg-white px-5 py-2.5 rounded-2xl shadow-xl text-sm font-black tracking-tight opacity-0 group-hover:opacity-100 transition-all -translate-x-4 group-hover:translate-x-0">
          Need a table? Chat with us
        </span>
        <div className="size-16 bg-[#25D366] text-white rounded-3xl flex items-center justify-center shadow-2xl shadow-[#25D366]/40 hover:scale-110 active:scale-95 transition-all relative overflow-hidden">
          <div className="absolute inset-0 bg-white/20 animate-pulse"></div>
          <svg className="size-9 relative z-10" fill="currentColor" viewBox="0 0 24 24">
            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z"/>
          </svg>
        </div>
      </a>
    </div>
  );
};

const App: React.FC = () => {
  return (
    <HashRouter>
      <AppContent />
    </HashRouter>
  );
};

export default App;
